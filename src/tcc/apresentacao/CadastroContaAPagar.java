private void carregarFormasdepagamento() {
       try {
           List<FormaPagamento> pagamentos = Pagar_contasController.listarFormaspagamento();
           for (FormaPagamento pagamento : pagamentos) {
               
               FormaPagamento.addItem(pagamento);
           }
       } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, "Não foi possivel mostrar as formas de pagamento!");
           ex.printStackTrace();
       }
    }

    private void limparDados() {
        valor.setText("");
        empresa.setText("");
       
    }


    private void valorCompraPecasKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_valorCompraPecasKeyReleased
        if ((evt.getKeyCode() < 48 || evt.getKeyCode() > 58) && (evt.getKeyCode() != 8 && evt.getKeyCode() != 46)) {
            if (evt.getKeyCode() < 96 || evt.getKeyCode() > 105) {
                if (valorCompraPecas.getText().length() > 0) {
                    valorCompraPecas.setText(valorCompraPecas.getText().
                            substring(0, valorCompraPecas.getText().length() - 1));
                }
            }
        }
        if (evt.getKeyCode() == 44 || evt.getKeyCode() == 110) {
            valorCompraPecas.setText(valorCompraPecas.getText() + ".");
        }

    }//GEN-LAST:event_valorCompraPecasKeyReleased


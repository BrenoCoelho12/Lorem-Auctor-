    private void valorCompraPecasFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_valorCompraPecasFocusLost
        if (botaoRevenda.isSelected()) {
            double cem = 100;
            BigDecimal cemBD = new BigDecimal(cem);
            BigDecimal margemLucro = new BigDecimal(margemLucroRevenda.getText().trim());
            BigDecimal valorCompra = new BigDecimal(valorCompraPecas.getText().trim());
            BigDecimal valorDaPorcentagemInformada = (valorCompra.multiply(margemLucro)).divide(cemBD);
            BigDecimal valorRevendaComAMargemInformada = valorCompra.add(valorDaPorcentagemInformada);
            valorRevendaPecas.setText(String.valueOf(valorRevendaComAMargemInformada));
        }
        if (botaoVarejo.isSelected()) {
            double cem = 100;
            BigDecimal cemBD = new BigDecimal(cem);
            BigDecimal margemLucro = new BigDecimal(margemLucroVarejo.getText().trim());
            BigDecimal valorCompra = new BigDecimal(valorCompraPecas.getText().trim());
            BigDecimal valorDaPorcentagemInformada = (valorCompra.multiply(margemLucro)).divide(cemBD);
            BigDecimal valorRevendaComAMargemInformada = valorCompra.add(valorDaPorcentagemInformada);
            valorVarejoPecas.setText(String.valueOf(valorRevendaComAMargemInformada));
        }


    }//GEN-LAST:event_valorCompraPecasFocusLost


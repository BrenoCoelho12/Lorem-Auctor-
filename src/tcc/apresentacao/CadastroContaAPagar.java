    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed

if(empresa.getText().equals("")) {
      JOptionPane.showMessageDialog(null, "Preencha o nome da Empresa", "Aviso", JOptionPane.WARNING_MESSAGE);
      return;
}
if(valor.getText().equals("")) {
      JOptionPane.showMessageDialog(null, "Preencha o valor da Conta", "Aviso", JOptionPane.WARNING_MESSAGE);
      return;
}
if(FormaPagamento == null) {
      JOptionPane.showMessageDialog(null, "Preencha a Forma de Pagamento", "Aviso", JOptionPane.WARNING_MESSAGE);
      return;
}

if(data_vencimento.getText().length() < 9) {
      JOptionPane.showMessageDialog(null, "Preencha a Data de Vencimento", "Aviso", JOptionPane.WARNING_MESSAGE);
      return;
}
if(contaBancaria.getText().equals("")) {
      JOptionPane.showMessageDialog(null, "Preencha o nome de alguém com uma conta bancaria válida. Se não for necessário, preencha c/ qualq. tecla", "Aviso", JOptionPane.WARNING_MESSAGE);
      return;
}
        
         ContasAPagar pc = new ContasAPagar();
        
        FormaPagamento forma_pagamento =  (FormaPagamento) FormaPagamento.getSelectedItem();
        BigDecimal valor1  = new BigDecimal(valor.getText().trim());
        
        
        
        pc.setEmpresa(empresa.getText().trim());
        pc.setFormaPagamento(forma_pagamento);
        pc.setValor(valor1);
        pc.setDataVencimento(data_vencimento.getText());
        pc.setContabancaria(contaBancaria.getText());
        pc.setSituacaoConta(situacao.getText());
       
        try {
            Pagar_contasController.CadastrarContas(pc);
            JOptionPane.showMessageDialog(null, "Conta cadastrada com sucesso!");
            limparDados();
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao salvar o cadastro da conta!");
        }
        
    }//GEN-LAST:event_salvarActionPerformed



    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed

        if (nomeProdutoPecas.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o nome do Produto", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (valorCompraPecas.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o valor de Compra do Produto", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (valorRevendaPecas.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o valor de Revenda", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (valorVarejoPecas.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o valor de Varejo do Produto", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (marca.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha a Marca", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (descricaoPecas.getSelectedItem().toString().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha a Descrição da peça", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (localizacaoProduto.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha a Localização do Produto", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (qtdEstoque.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha a quantidade de estoque do Produto", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Produtos produto = new Produtos();

        BigDecimal campoValorRevenda = new BigDecimal((valorRevendaPecas.getText().trim()));
        BigDecimal campoValorVarejo = new BigDecimal((valorVarejoPecas.getText().trim()));
        BigDecimal campoValorCompra = new BigDecimal((valorCompraPecas.getText().trim()));
        int quantidadeEstoque = Integer.parseInt(qtdEstoque.getText().trim());
        TipoProduto tipoproduto = (TipoProduto) descricaoPecas.getSelectedItem();

        produto.setNomeProduto(nomeProdutoPecas.getText().trim());
        produto.setPrecoVarejo(campoValorVarejo);
        produto.setPrecoRevenda(campoValorRevenda);
        produto.setPrecoDeCompra(campoValorCompra);
        produto.setDescricao(tipoproduto);
        produto.setLocalizador(localizacaoProduto.getText().trim());
        produto.setMarca(marca.getText().trim());
        produto.setUnidade(unidade.getText().trim());
        produto.setQuantidadeEstoque(quantidadeEstoque);

        try {
            ProdutosController.Cadastrarpecas(produto);
            JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso!");
            limparDados();
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao salvar o cadastro do produto!");
        }
        limparDados();
    }//GEN-LAST:event_salvarActionPerformed

